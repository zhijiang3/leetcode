import multiply from "./solution.js";

test("example 1", () => {
  expect(multiply("2", "3")).toBe("6");
});

test("example 2", () => {
  expect(multiply("123", "456")).toBe("56088");
});

test("大数字相乘", () => {
  expect(
    multiply(
      "111111111111111111111111111111111111111111111111111111111111",
      "22222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222"
    )
  ).toBe(
    "2469135802469135802469135802469135802469135802469135802469133333333333333333333333333333333333333333333333333333333333333333333308641975308641975308641975308641975308641975308641975308642"
  );
});

test("数字0相乘", () => {
  expect(multiply("123", "0")).toBe("0");
  expect(multiply("0", "111")).toBe("0");
});
